inline const Glyph5x7& LookupGlyph(char c) {
    char key = static_cast<char>(std::toupper(static_cast<unsigned char>(c)));
    for (const Glyph5x7& glyph : GlyphTable()) {
        if (glyph.character == key) {
            return glyph;
        }
    }
    return GlyphTable().front();
}

template <typename PixelWriter>
inline void RasterizeGlyph(char c, int x, int y, int scale, const PixelWriter& write_pixel) {
    const Glyph5x7& glyph = LookupGlyph(c);
    for (int row = 0; row < 7; ++row) {
        for (int col = 0; col < 5; ++col) {
            if (((glyph.rows[row] >> (4 - col)) & 1U) == 0U) {
                continue;
            }
            for (int dy = 0; dy < scale; ++dy) {
                for (int dx = 0; dx < scale; ++dx) {
                    write_pixel(x + col * scale + dx, y + row * scale + dy);
                }
            }
        }
    }
}

